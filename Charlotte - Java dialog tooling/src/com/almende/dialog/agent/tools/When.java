package com.almende.dialog.agent.tools;

public class When{
	public When(){};
	long start;
	long end;
	public long getStart() {
		return start;
	}
	public long getEnd() {
		return end;
	}
	public void setStart(long start) {
		this.start = start;
	}
	public void setEnd(long end) {
		this.end = end;
	}
}
